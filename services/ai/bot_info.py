def get_bot_info(key=None):
    """
    Получает информацию о боте.

    Args:
        key: Ключ для получения конкретной информации (None для получения всей информации)

    Returns:
        dict or str: Запрошенная информация о боте
    """
    if key is None:
        return BOT_INFO
    return BOT_INFO.get(key, "Информация не найдена")


